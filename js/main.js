
$(function () {
    /*=================================================
    ハンバーガーメニュー
    ===================================================*/

    //下記も同様の意味となります。
    $(".toggle-btn").on("click", function () {
        $("header").toggleClass("open")
    })

    // メニューが表示されている時に画面をクリックした場合
    $('#mask').on('click', function () {
        // openクラスを削除して、メニューを閉じる
        $('header').removeClass('open')
    })
});